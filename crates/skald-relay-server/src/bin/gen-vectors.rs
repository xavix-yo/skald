//! Reference generator for the crypto interop test vectors
//! (data/ios-app/test-vectors.md §3). This is the *source of truth*: run it once
//! and paste the output into test-vectors.md §4, then commit.
//!
//!   cargo run -p skald-relay-server --bin gen-vectors
//!
//! The relay itself only verifies Ed25519 and derives namespace_id; the full
//! E2E suite (X25519/HKDF/AES-GCM) lives here so independent implementations
//! (Swift app, Kotlin app) can assert byte-for-byte equality.

use aes_gcm::aead::{Aead, Payload};
use aes_gcm::{Aes256Gcm, KeyInit, Nonce};
use base64::{Engine, engine::general_purpose::STANDARD as B64};
use ed25519_dalek::{Signer, SigningKey};
use hkdf::Hkdf;
use sha2::{Digest, Sha256};
use x25519_dalek::{PublicKey, StaticSecret};

const AUTH_DOMAIN: &[u8] = b"skald-relay-auth-v1";
const NS_DOMAIN: &[u8] = b"skald-namespace-v1";

// Exact UTF-8 plaintexts from test-vectors.md §1 (no spaces, no field reorder).
const PLAINTEXT_A2C: &[u8] = br#"{"v":1,"kind":"inbox_update","id":"00000000-0000-4000-8000-000000000001","ts":1750000000000,"badge":1,"approvals":[{"request_id":"appr_test_1","tool_name":"send_email","agent_label":"Skald","summary":"Test","created_at":1750000000000}],"clarifications":[]}"#;
const PLAINTEXT_C2A: &[u8] = br#"{"v":1,"kind":"approval_response","id":"00000000-0000-4000-8000-000000000002","ts":1750000000000,"request_id":"appr_test_1","decision":"approved"}"#;

fn hkdf32(ikm: &[u8], salt: &[u8], info: &[u8]) -> [u8; 32] {
    let hk = Hkdf::<Sha256>::new(Some(salt), ikm);
    let mut out = [0u8; 32];
    hk.expand(info, &mut out).unwrap();
    out
}

fn main() {
    let seed_a: [u8; 32] = (0u8..32).collect::<Vec<_>>().try_into().unwrap();
    let seed_c: [u8; 32] = (32u8..64).collect::<Vec<_>>().try_into().unwrap();

    // --- key derivation (crypto.md §3) ---
    let xa_priv = hkdf32(&seed_a, b"skald-kdf-v1", b"x25519");
    let ea_priv = hkdf32(&seed_a, b"skald-kdf-v1", b"ed25519");
    let xc_priv = hkdf32(&seed_c, b"skald-kdf-v1", b"x25519");
    let ec_priv = hkdf32(&seed_c, b"skald-kdf-v1", b"ed25519");

    let xa = StaticSecret::from(xa_priv);
    let xc = StaticSecret::from(xc_priv);
    let xa_pub = PublicKey::from(&xa).to_bytes();
    let xc_pub = PublicKey::from(&xc).to_bytes();

    let ea = SigningKey::from_bytes(&ea_priv);
    let ec = SigningKey::from_bytes(&ec_priv);
    let ea_pub = ea.verifying_key().to_bytes();
    let ec_pub = ec.verifying_key().to_bytes();

    // --- namespace_id (crypto.md §7) ---
    let mut h = Sha256::new();
    h.update(NS_DOMAIN);
    h.update([0u8]);
    h.update(ea_pub);
    let ns_raw: [u8; 32] = h.finalize().into();

    // --- ECDH + AEAD key (crypto.md §4-5), with the mandatory self-check ---
    let s1 = xa.diffie_hellman(&PublicKey::from(xc_pub));
    let s2 = xc.diffie_hellman(&PublicKey::from(xa_pub));
    assert_eq!(s1.as_bytes(), s2.as_bytes(), "ECDH mismatch");
    let shared = *s1.as_bytes();
    let aes_key = hkdf32(&shared, b"skald-session-v1", b"aes-256-gcm");
    let cipher = Aes256Gcm::new_from_slice(&aes_key).unwrap();

    // --- A2C (agent → client) ---
    let mut n_a2c = [0u8; 12];
    n_a2c[..4].copy_from_slice(&[0, 0, 0, 1]);
    n_a2c[4..].copy_from_slice(&1u64.to_be_bytes());
    let mut aad_a2c = Vec::new();
    aad_a2c.extend_from_slice(&ns_raw);
    aad_a2c.extend_from_slice(&ea_pub);
    aad_a2c.extend_from_slice(&ec_pub);
    let sealed_a2c = cipher
        .encrypt(
            Nonce::from_slice(&n_a2c),
            Payload {
                msg: PLAINTEXT_A2C,
                aad: &aad_a2c,
            },
        )
        .unwrap();

    // --- C2A (client → agent) ---
    let mut n_c2a = [0u8; 12];
    n_c2a[..4].copy_from_slice(&[0, 0, 0, 2]);
    n_c2a[4..].copy_from_slice(&1u64.to_be_bytes());
    let mut aad_c2a = Vec::new();
    aad_c2a.extend_from_slice(&ns_raw);
    aad_c2a.extend_from_slice(&ec_pub);
    aad_c2a.extend_from_slice(&ea_pub);
    let sealed_c2a = cipher
        .encrypt(
            Nonce::from_slice(&n_c2a),
            Payload {
                msg: PLAINTEXT_C2A,
                aad: &aad_c2a,
            },
        )
        .unwrap();

    // --- auth signature (client), crypto.md §8 ---
    let challenge =
        hex::decode("aabbccddeeff00112233445566778899aabbccddeeff00112233445566778899").unwrap();
    let mut m = Vec::new();
    m.extend_from_slice(AUTH_DOMAIN);
    m.push(0);
    m.extend_from_slice(&challenge);
    let sig = ec.sign(&m);

    // Round-trip self-check: decrypt what we sealed.
    let dec = cipher
        .decrypt(
            Nonce::from_slice(&n_a2c),
            Payload {
                msg: &sealed_a2c,
                aad: &aad_a2c,
            },
        )
        .unwrap();
    assert_eq!(dec, PLAINTEXT_A2C, "A2C round-trip mismatch");

    println!("# Generated by `cargo run -p skald-relay-server --bin gen-vectors`");
    println!("V1  agent_x25519_priv  = {}", hex::encode(xa_priv));
    println!("V2  agent_x25519_pub   = {}", hex::encode(xa_pub));
    println!("V3  agent_ed25519_priv = {}", hex::encode(ea_priv));
    println!("V4  agent_ed25519_pub  = {}", hex::encode(ea_pub));
    println!("V5  client_x25519_priv = {}", hex::encode(xc_priv));
    println!("V6  client_x25519_pub  = {}", hex::encode(xc_pub));
    println!("V7  client_ed25519_priv= {}", hex::encode(ec_priv));
    println!("V8  client_ed25519_pub = {}", hex::encode(ec_pub));
    println!("V9  namespace_id       = {}", hex::encode(ns_raw));
    println!("V10 shared_secret      = {}", hex::encode(shared));
    println!("V11 aes_key            = {}", hex::encode(aes_key));
    println!("V12 nonce_a2c          = {}", hex::encode(n_a2c));
    println!("V13 aad_a2c            = {}", hex::encode(&aad_a2c));
    println!("V14 sealed_a2c (b64)   = {}", B64.encode(&sealed_a2c));
    println!("V15 nonce_c2a          = {}", hex::encode(n_c2a));
    println!("V16 aad_c2a            = {}", hex::encode(&aad_c2a));
    println!("V17 sealed_c2a (b64)   = {}", B64.encode(&sealed_c2a));
    println!("V18 auth_sig_client    = {}", hex::encode(sig.to_bytes()));
}
